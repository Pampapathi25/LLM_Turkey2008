# Cell 5: Clean extracted text

def clean_text(text):
    text = text.replace("\x0c", " ")
    text = re.sub(r"\s+", " ", text)
    text = re.sub(r"(\w)-\s+(\w)", r"\1\2", text)
    return text.strip()

page_df["clean_text"] = page_df["text"].apply(clean_text)

full_text = " ".join(page_df["clean_text"].tolist())

