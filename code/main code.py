# Cell 7: Ollama helper function

def ask_ollama(prompt, model="llama3.2:3b"):
    if ollama is None:
        return "Ollama package not installed."

    response = ollama.chat(
        model=model,
        messages=[
            {
                "role": "system",
                "content": "You are a careful AI assistant. Use only the provided report text. Do not invent facts."
            },
            {
                "role": "user",
                "content": prompt
            }
        ]
    )

    return response["message"]["content"]

